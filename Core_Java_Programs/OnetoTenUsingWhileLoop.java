package com.mkpits.java.basicprograms;
import java.util.*;
public class OnetoTenUsingWhileLoop {
    public static void main(String[] args)
    {
        int num=1;
        while(num<=10){
            System.out.println("number "+num);
            num++;

        }
    }
}
